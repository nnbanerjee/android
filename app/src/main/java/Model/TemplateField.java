package Model;


public class TemplateField {

    private String fieldId;
    private String fieldName;
    private String templateFieldcol;
    private String value;
    private String templateId;

    /**
     *
     * @return
     * The fieldId
     */
    public String getFieldId() {
        return fieldId;
    }

    /**
     *
     * @param fieldId
     * The fieldId
     */
    public void setFieldId(String fieldId) {
        this.fieldId = fieldId;
    }

    /**
     *
     * @return
     * The fieldName
     */
    public String getFieldName() {
        return fieldName;
    }

    /**
     *
     * @param fieldName
     * The fieldName
     */
    public void setFieldName(String fieldName) {
        this.fieldName = fieldName;
    }

    /**
     *
     * @return
     * The templateFieldcol
     */
    public String getTemplateFieldcol() {
        return templateFieldcol;
    }

    /**
     *
     * @param templateFieldcol
     * The templateFieldcol
     */
    public void setTemplateFieldcol(String templateFieldcol) {
        this.templateFieldcol = templateFieldcol;
    }

    /**
     *
     * @return
     * The value
     */
    public String getValue() {
        return value;
    }

    /**
     *
     * @param value
     * The value
     */
    public void setValue(String value) {
        this.value = value;
    }

    /**
     *
     * @return
     * The templateId
     */
    public String getTemplateId() {
        return templateId;
    }

    /**
     *
     * @param templateId
     * The templateId
     */
    public void setTemplateId(String templateId) {
        this.templateId = templateId;
    }

}